package collection;

public class Student 
{
	int id;
	String name;
	int roll;
	public Student() 
	{
		super();
	}
	public int getId() 
	{
		return id;
	}
	public String getName()
	{
		return name;
	}
	public int getRoll() 
	{
		return roll;
	}
	public Student(int id, String name, int roll) {
		super();
		this.id = id;
		this.name = name;
		this.roll = roll;
	}
	
	
	
	
	
}
